struct Pair<T> {
    x: T,
    y: T,
}

impl<T> Pair<T> {
    #[allow(unused)]
    fn new(x: T, y: T) -> Self {
        Self { x, y }
    }
}

#[allow(unused)]
impl<T: Display + PartialOrd> Pair<T> {
    fn cmp_display(&self) {
        if self.x >= self.y {
            println!("The largest member is x = {}", self.x);
        } else {
            println!("The largest member is y = {}", self.x);
        }
    }
}
